
public class Data_Types {

	public static void main(String[] args) {
		System.out.println("Data Types in JAVA\n");
		System.out.println("1. byte(integer,1 byte)");
		System.out.println("2. short(integer,2 byte)");
		System.out.println("3. int(integer,4 byte)");
		System.out.println("4. long(integer,8 byte)");
		System.out.println("5. float(float,4 byte)");
		System.out.println("6. double(float,1 byte)");
		System.out.println("7. char(a char,2 byte)");
		System.out.println("8. boolean(true/false,1 bit)");
		//By default float stores double value
		//i.e typecasting is needed
		//e.g
		float a=(float)3.5;
		System.out.println("This is a typecasted float : "+a+"\nWithout typecasting float stores a double value");
		//This an example of cancatenation
	}

}
